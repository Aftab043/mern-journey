let months = ['january', 'july', 'march', 'august'];
console.log("initial array:", months)
// push
months.push("may");
console.log("after push:",months);
// pop
months.pop();
console.log("after pop:", months);
// unshift
months.unshift("december");
console.log("after unshift:", months);
//shift
months.shift();
console.log("after shift:", months);
//After Operation
console.log("final array:", months);