let popcorn_size = 'LL';
if(popcorn_size === 'XL'){
    console.log(`price is popcorn size ${popcorn_size} Rs. 250`);
}
else if(popcorn_size === 'L'){
    console.log(`price is popcorn size ${popcorn_size} Rs. 200`);
}
else if(popcorn_size === 'M'){
    console.log(`price is popcorn size ${popcorn_size} Rs. 100`);
}
else if(popcorn_size === 'S'){
    console.log(`price is popcorn size ${popcorn_size} Rs. 50`);
}
else{
    console.log(`popcorn size is invalid ${popcorn_size} !`)
}